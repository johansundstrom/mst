            <h1>Regulations for driving on public roads</h1>
            <p> Based on the defined delay for the car to come to a stop the braking
                 momentum for the wheels can be calculated. This then leads to the
                  choice of braking discs, braking blocks and main cylinders of the
                   braking system. Stated more simply, this way one can calculate the
                    force that needs to be applied to the braking pedal in order to
                     come to a stop within the defined distance.When the axial force
                     to be applied on the main cylinders is known the braking pedal’s
                     lever can be adjusted for the driver to be able to push down the
                     pedal.  </p>
            
            <p>The solar car’s breaking system is similar to what can be found in a 
                commercial car, although specific components are chosen. For example 
                the braking blocks used in bicycles are used for the back wheels while 
                gokart braking blocks are used for the front wheels.  </p>


