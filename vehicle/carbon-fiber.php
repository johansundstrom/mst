            <h1 class="h1">Why carbonfiber?</h1>

            <p class="lead">
                Carbon fiber is mainly chosen for weight reasons.
                Less weight is less energy waste. Carbon fiber is also a sturdy material which can withstand impacts
                from several directions. Glass fiber is also an option because it is more cost effective. However, glass
                fibres density is more than twice the density of carbon fibres and therfore signiciantly heavier.
            </p>

            <p>Carbon fiber is one of the most wonderful materials we use for our solar car. It is a material of quite
                extraordinary properties that has led to be one of our most favored materials used to sculpt our
                prodigious Viking.</p>

            <p>When constructing a race car which is going to travel thousands of kilometers through the vast spaces of
                Australia, you will need only the best materials available. Materials that are strong, for rigidity but
                also materials that are light weight enough for optimized performance.</p>

            <p>Carbon fiber is the tremendous material that takes the quantum leap to qualify best when analyzing the
                criteria. It is a material of both great rigidity, but also low weight. This goes hand in hand with our
                values, at the same time, carbon fiber allows us to shape and form the parts in any way we desire. Such
                as an aerodynamically optimized vehicle.</p>

            <p>On top of being strong and light weight, carbon fiber is also high in chemical resistance, is temperature
                tolerant at excessive heat but also has a low thermal expansion. This benefits us under the scorching
                sun in the outback of Australia.</p>

            <p>Other examples of what carbon fiber is used for is: aeroplane wings, bike frames and propeller blades.
                These are all products that demand materials with low weight to strength ratio.</p>

